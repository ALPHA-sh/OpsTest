
public class OperationsPractice {

	public static void main(String[] args) 
	{
		int var1;
		var1 = 100;
		System.out.println("original: " + var1 );
		var1 += 5;
		System.out.println("+5 = " + var1 );
		var1 /= 2;
		System.out.println("/2 = " +  var1 );
		var1 *= 4;
		System.out.println("*4 = " + var1 );
		var1 -= 6;
		System.out.println("-6 = " + var1 );
		var1 %= 2;
		System.out.println("%2 = " + var1 + " (end result)"); //iuuiqweyriouwyriouqwryoi
	}

}
